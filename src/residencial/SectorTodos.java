    public SectorTodos() {
        initComponents();
        this.setLocationRelativeTo(null);
        String texto = "";
        for (Cliente cliente : Login.clientes) {
            if (cliente.montoAPagar==1000000) {
                texto += cliente.toString() + "\nSector A\n__________________\n";
            } else if(cliente.montoAPagar==750000){
                texto += cliente.toString() + "\nSector B\n__________________\n";
            } else{
                texto += cliente.toString() + "\nSector C\n__________________\n";
            }
                
        }
        if (texto.equals("")) {
            texto = " Asegurese de estar ingresando correctamente \nla informacion, Gracias.";
        }
        ResultadoClientes.setText(texto);
        ResultadoClientes.getAutoscrolls();
    }

