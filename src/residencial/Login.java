    public Login() {
        initComponents();
        this.setLocationRelativeTo(null);

        Date date = new Date();
        int dia = date.getDay();
        String nombreDia;
        if (dia == 0) {        nombreDia = "Domingo";
        } else if (dia == 1) { nombreDia = "Lunes";
        } else if (dia == 2) { nombreDia = "Martes";
        } else if (dia == 3) { nombreDia = "Miercoles";
        } else if (dia == 4) { nombreDia = "Jueves";
        } else if (dia == 5) { nombreDia = "Viernes";  } else {nombreDia = "Sabado";
        }
        String texto = nombreDia;
        texto = texto + "  " + String.valueOf(date.getDate()) + " / " + String.valueOf(date.getMonth() + 1) + " / "
                + String.valueOf((1900 + date.getYear()));
        fecha.setText(texto);
        Hora h = new Hora(hora);
        h.start();
        try {
            BufferedReader br = new BufferedReader(new FileReader("./clientes.txt"));
            String line = br.readLine();
            int id;
            String nombre;
            String apellidos;
            String genero;
            int edad;
            int monto;
            boolean familia;
            while (line != null) {
                id = Integer.parseInt(line);
                line = br.readLine();
                nombre = line;
                line = br.readLine();
                
                apellidos = line;
                line = br.readLine();
                genero = line;
                line = br.readLine();
                edad = Integer.parseInt(line);
                line = br.readLine();
                monto = Integer.parseInt(line);
                line = br.readLine();
                if (Integer.parseInt(line) == 1) {
                    familia = true;
                } else {
                    familia = false;
                }
                switch (monto) {
                    case 1000000:
                        Sector.contadorSectorA++;
                        clientes.add(new Cliente(monto, familia, id, nombre, apellidos, genero, edad));
                        break;
                    case 750000:
                        Sector.contadorSectorB++;
                        clientes.add(new Cliente(monto, familia, id, nombre, apellidos, genero, edad));
                        break;
                    case 550000:
                        Sector.contadorSectorC++;
                        clientes.add(new Cliente(monto, familia, id, nombre, apellidos, genero, edad));
                        break;
                    default:
                        JOptionPane.showMessageDialog(null, "Un cliente fue mal registrado debido a que tenia un monto equivocado");
                        break;
                }

                line = br.readLine();
            }
        } catch (IOException | NumberFormatException | NullPointerException ex) {
            JOptionPane.showMessageDialog(null, "Hubo un error en la lectura de los clientes");
        }
    }

